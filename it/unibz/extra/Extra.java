package it.unibz.extra;

public class Extra {
    private int code;
    private int unitaryPrice;

    public Extra(int code, int unitaryPrice) {
        this.code = code;
        this.unitaryPrice = unitaryPrice;
    }

    public int getCode() {
        return code;
    }

    public int getUnitaryPrice() {
        return unitaryPrice;
    }
}
